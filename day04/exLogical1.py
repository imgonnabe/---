# 논리합 테스트
print(False or False)
print(False or True)
print(True or False)
print(True or True)

# 논리곱 테스트
print(False and False)
print(False and True)
print(True and False)
print(True and True)

# 논리 부정 테스트
print(not False)
print(not True)
print(not not True)