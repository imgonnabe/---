number=7 #1~5

if number==1:
    print('number는 1입니다.')
elif number==2:
    print('number는 2입니다.')
elif number==3:
    print('number는 3입니다.')
elif number==4:
    print('number는 4입니다.')
elif number==5:
    print('number는 5입니다.')
else:
    print('number는 1~5가 아닙니다.')
print('프로세스 종료')    